    private void bAdaugaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bAdaugaActionPerformed
        try{
            Abonat a = new Abonat(ftNume.getText(),ftPrenume.getText(),ftCnp.getText(),ftNrTel.getText());
            String nume = a.getNume();
            String prenume = a.getPrenume();
            String cnp = a.getCnp();
            String nrTel = a.getNumar();
            
            Object[] row = {nume, prenume, cnp, nrTel};
            
            agenda.addAbonat(a);
            
            
        }catch(IllegalArgumentException e){
            JOptionPane.showMessageDialog(
                        this,
                        e.getMessage(),
                        "Eroare!",
                        JOptionPane.ERROR_MESSAGE
                );
            if(null != e.getMessage())switch (e.getMessage()) {
                case "Numele introdus nu este corect!":
                    ftNume.setText("");
                    break;
                case "Prenumele introdus nu este corect!":
                    ftPrenume.setText("");
                    break;
                case "CNP-ul introdus nu este valid.":
                    ftCnp.setText("");
                    break;
                case "Numarul de telefon nu este valid.":
                    ftNrTel.setText("");
                    break;
            }
        }
        
    }//GEN-LAST:event_bAdaugaActionPerformed

