    private void bd3FiltreazaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bd3FiltreazaActionPerformed
        String nume  = tfd3Nume.getText();
        String prenume = tfd3Prenume.getText();
        String cnp = tfd3Cnp.getText();
        String numar = tfd3NrTel.getText();
        final TableRowSorter<CarteDeTelefon> sorter = new TableRowSorter<CarteDeTelefon>(agenda);
        jTable.setRowSorter(sorter);
        java.util.List<RowFilter<Object,Object>> filters = new ArrayList<RowFilter<Object,Object>>(4);
        filters.add(RowFilter.regexFilter("(?i)" + nume,0));
        filters.add(RowFilter.regexFilter("(?i)" + prenume,1));
        filters.add(RowFilter.regexFilter("(?i)" + cnp,2));
        filters.add(RowFilter.regexFilter("(?i)"+ numar,3));
        RowFilter<Object,Object> serviceFilter = RowFilter.andFilter(filters);
        sorter.setRowFilter(serviceFilter);
    
        
    }//GEN-LAST:event_bd3FiltreazaActionPerformed

